function tryTranslate(s) {
  if (typeof s !== 'string' || !/[一-鿿]/.test(s)) return null
  if (T[s]) return T[s]
  let out = s
  for (const [zh, en] of Object.entries(T).sort((a, b) => b[0].length - a[0].length)) {
    if (out.includes(zh)) out = out.split(zh).join(en)
  }
  if (!/[一-鿿]/.test(out)) return out
  return null
}

let translated = 0
const walk = (o) => {
  for (const k of Object.keys(o || {})) {
    const v = o[k]
    if (typeof v === 'string' && /[一-鿿]/.test(v)) {
      const t = tryTranslate(v)
      if (t !== null) {
        o[k] = t
        translated++
      }
    } else if (typeof v === 'object' && v !== null) {
      walk(v)
    }
  }
}
walk(en)

