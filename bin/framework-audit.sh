#!/usr/bin/env bash
# Framework audit (script) + stamp update
#
# Outputs:
# - PASS/FAIL report: osg-spec-docs/tasks/audit/framework-audit-YYYY-MM-DD.md
# - On PASS: updates framework context stamp
#   osg-spec-docs/tasks/audit/framework-context-stamp.json

set -euo pipefail

# Cross-platform Python 3 (python3 | py -3 | python)
source "$(dirname "${BASH_SOURCE[0]}")/lib-python.sh"
require_py3

DATE_STR="$(date +%Y-%m-%d)"
AUDIT_DIR="osg-spec-docs/tasks/audit"
REPORT_PATH="${AUDIT_DIR}/framework-audit-${DATE_STR}.md"

mkdir -p "${AUDIT_DIR}"

fail_count=0

report() {
  printf '%s\n' "$1"

  tee -a "${REPORT_PATH}" >/dev/null
}

section() {
  report "## $1"
}

pass() {
  report "- ✅ $1"
}

fail() {
  local msg="$1"
  report "- ❌ $msg"
  fail_count=$((fail_count + 1))
}

line_ref() {
  local file="$1"
  local pattern="$2"
  # Print line numbers for pattern (best-effort)
  if command -v rg >/dev/null 2>&1; then
    rg -n --no-heading "${pattern}" "${file}" 2>/dev/null | head -n 5 || true
  else
    # Fallback: grep -n
    grep -nE "${pattern}" "${file}" 2>/dev/null | head -n 5 || true
  fi
}

# Fresh report
: >"${REPORT_PATH}"
report "# Framework Audit (${DATE_STR})"
report "Generated by: bash bin/framework-audit.sh"

# -------------------------------------------------------------------
# Dimension 1: 状态管理一致性
# -------------------------------------------------------------------
section "1. 状态管理一致性"

# 1.1 phase 字段清理（.claude/ 内 state.phase 出现 0 次，log.yaml execution_log.phase 允许）
# We'll exclude .claude/templates/log.yaml and any line containing execution_log:
# (best-effort rule; aim to catch accidental regressions)
phase_hits="$({
  if command -v rg >/dev/null 2>&1; then
    rg -n "state\\.phase" .claude \
      --glob '!**/log.yaml' \
      --glob '!.claude/skills/framework-audit/SKILL.md' || true
  else
    grep -R -n "state.phase" .claude 2>/dev/null \
      | grep -v "templates/log.yaml" \
      | grep -v "skills/framework-audit/SKILL.md" || true
  fi
} | head -n 20)"
if [[ -z "${phase_hits}" ]]; then
  pass "no state.phase under .claude (excluding log.yaml)"
else
  fail "found state.phase under .claude (excluding log.yaml); sample:\n\n\`\`\`\n${phase_hits}\n\`\`\`"
fi

# 1.2 STATE.yaml 实际文件中无 phase 字段
if py3 - <<'PY' >/dev/null 2>&1
import yaml
from pathlib import Path
p = Path('osg-spec-docs/tasks/STATE.yaml')
data = yaml.safe_load(p.read_text(encoding='utf-8')) or {}
assert 'phase' not in data
PY
then
  pass "STATE.yaml has no top-level phase"
else
  fail "STATE.yaml contains unexpected top-level 'phase'"
fi

# 1.3 state/checkpoint 模板 workflow 结构与 STATE.yaml 一致（只校验 workflow.* key set）
if py3 - <<'PY' >/dev/null 2>&1
import yaml
from pathlib import Path

def keys(obj):
    return set((obj or {}).keys()) if isinstance(obj, dict) else set()

state = yaml.safe_load(Path('osg-spec-docs/tasks/STATE.yaml').read_text(encoding='utf-8')) or {}
wf_state = keys(state.get('workflow'))

tmpl_state = yaml.safe_load(Path('.claude/templates/state.yaml').read_text(encoding='utf-8')) or {}
wf_tmpl_state = keys(tmpl_state.get('workflow'))

chk = yaml.safe_load(Path('.claude/templates/checkpoint.yaml').read_text(encoding='utf-8')) or {}
wf_chk = keys(((chk.get('state_snapshot') or {}) if isinstance(chk, dict) else {}).get('workflow'))

assert wf_state and wf_tmpl_state and wf_chk
assert wf_state == wf_tmpl_state == wf_chk
PY
then
  pass "workflow key set matches across STATE.yaml, templates/state.yaml, templates/checkpoint.yaml"
else
  fail "workflow key set mismatch across STATE.yaml and templates (state/checkpoint)"
fi

# -------------------------------------------------------------------
# Dimension 2: 工作流链路完整性（最小脚本校验：state-machine.yaml contains required mappings)
# -------------------------------------------------------------------
section "2. 工作流链路完整性"

if py3 - <<'PY' >/dev/null 2>&1
import yaml
from pathlib import Path
sm = yaml.safe_load(Path('.claude/skills/workflow-engine/state-machine.yaml').read_text(encoding='utf-8')) or {}
states = sm.get('states') or {}
required_states = [
  'not_started','brainstorm_done','brainstorm_pending_confirm','story_split_done','stories_approved',
  'ticket_split_done','tickets_approved','implementing','ticket_done','all_tickets_done',
  'story_verified','verification_failed','story_done','story_approved','all_stories_done'
]
for s in required_states:
    assert s in states, s
    assert 'next_action' in (states.get(s) or {}), s

approval = sm.get('approval_config_keys') or {}
required_approval_actions = {
  'approve_brainstorm','approve_stories','approve_tickets','approve_story','next'
}
assert required_approval_actions.issubset(set(approval.keys()))
PY
then
  pass "state-machine.yaml contains required states + approval_config_keys"
else
  fail "state-machine.yaml missing required states or approval_config_keys"
fi

# -------------------------------------------------------------------
# Dimension 3: CLAUDE.md 自洽性（最小：session start fields exist)
# -------------------------------------------------------------------
section "3. CLAUDE.md 自洽性"

# Check that SessionStart referenced fields exist in STATE.yaml (project/current_story/current_ticket/workflow.current_step/workflow.next_step)
if py3 - <<'PY' >/dev/null 2>&1
import yaml
from pathlib import Path
state = yaml.safe_load(Path('osg-spec-docs/tasks/STATE.yaml').read_text(encoding='utf-8')) or {}
assert 'project' in state
assert 'current_story' in state
assert 'current_ticket' in state
wf = state.get('workflow') or {}
assert 'current_step' in wf
assert 'next_step' in wf
PY
then
  pass "STATE.yaml contains SessionStart referenced fields"
else
  fail "STATE.yaml missing fields referenced by SessionStart template"
fi

# -------------------------------------------------------------------
# Dimension 4: Type 系统一致性（ticket template vs CLAUDE.md agent mapping)
# -------------------------------------------------------------------
section "4. Type 系统一致性"

if py3 - <<'PY' >/dev/null 2>&1
import re
from pathlib import Path

ticket = Path('.claude/templates/ticket.yaml').read_text(encoding='utf-8')
claude = Path('.claude/CLAUDE.md').read_text(encoding='utf-8')

m = re.search(r'^type:\s*backend\s*#\s*(.+)$', ticket, flags=re.M)
assert m, 'ticket.yaml type comment missing'
# Extract 'backend | frontend | ...'
parts = [p.strip() for p in m.group(1).split('|')]
parts = [p for p in parts if p]
assert parts, 'no types found'

# Ensure CLAUDE.md table includes each type (simple contains check)
for t in parts:
    assert t in claude, f'missing type in CLAUDE.md: {t}'
PY
then
  pass "ticket type enums appear in CLAUDE.md"
else
  fail "ticket type enums mismatch with CLAUDE.md"
fi

# -------------------------------------------------------------------
# Dimension 5: 废弃概念清理
# -------------------------------------------------------------------
section "5. 废弃概念清理"

depr_patterns=(
  "estimate_context_usage",
  "dispatch_openai",
  "OPENAI_API_KEY",
)
for pat in "${depr_patterns[@]}"; do
  hits="$({
    if command -v rg >/dev/null 2>&1; then
      rg -n "${pat}" .claude || true
    else
      grep -R -n "${pat}" .claude 2>/dev/null || true
    fi
  } | head -n 5)"
  if [[ -z "${hits}" ]]; then
    pass "no deprecated pattern: ${pat}"
  else
    fail "deprecated pattern found: ${pat}; sample:\n\n\`\`\`\n${hits}\n\`\`\`"
  fi

done

# Disallow '|| true' inside .claude (best-effort) - matches audit rule
hits_or_true="$({
  if command -v rg >/dev/null 2>&1; then
    rg -n "\\|\\|\\s*true" .claude || true
  else
    grep -R -nE "\\|\\|[[:space:]]*true" .claude 2>/dev/null || true
  fi
} | head -n 5)"
if [[ -z "${hits_or_true}" ]]; then
  pass "no '|| true' under .claude"
else
  fail "found '|| true' under .claude; sample:\n\n\`\`\`\n${hits_or_true}\n\`\`\`"
fi

# -------------------------------------------------------------------
# Dimension 6: 跨文件引用一致性（counts + approval keys)
# -------------------------------------------------------------------
section "6. 跨文件引用一致性"

# Count files
skills_count="$(ls .claude/skills/*/SKILL.md 2>/dev/null | wc -l | tr -d ' ')"
agents_count="$(ls .claude/agents/*.md 2>/dev/null | wc -l | tr -d ' ')"
commands_count="$(ls .claude/commands/*.md 2>/dev/null | wc -l | tr -d ' ')"

pass "skills SKILL.md count=${skills_count}"
pass "agents md count=${agents_count}"
pass "commands md count=${commands_count}"

# approval keys: must be exactly the 5 keys
if py3 - <<'PY' >/dev/null 2>&1
import yaml
from pathlib import Path
cfg = yaml.safe_load(Path('.claude/project/config.yaml').read_text(encoding='utf-8')) or {}
appr = cfg.get('approval') or {}
keys = set(appr.keys())
expected = {'brainstorm_confirm','story_split','ticket_split','ticket_done','story_done'}
assert keys == expected, (keys, expected)
PY
then
  pass "config.yaml approval keys match expected 5 keys"
else
  fail "config.yaml approval keys mismatch (expected 5 keys)"
fi

# -------------------------------------------------------------------
# Dimension 7: Markdown code fence parity
# -------------------------------------------------------------------
section "7. 代码块格式完整性"

# Check all markdown files under .claude for even number of ``` fences.
if py3 - <<'PY' >/dev/null 2>&1
from pathlib import Path

bad = []
for p in Path('.claude').rglob('*.md'):
    text = p.read_text(encoding='utf-8', errors='ignore')
    if text.count('```') % 2 != 0:
        bad.append(p.as_posix())

if bad:
    raise SystemExit(1)
PY
then
  pass "all .claude markdown files have balanced code fences"
else
  fail "found .claude markdown files with unbalanced code fences"
fi

# -------------------------------------------------------------------
# Summary + stamp update
# -------------------------------------------------------------------
section "Summary"
if (( fail_count == 0 )); then
  report "✅ PASS (7/7)"
  report "Stamp update: running framework_context_stamp_guard.py --mode update"
  py3 .claude/skills/workflow-engine/tests/framework_context_stamp_guard.py --mode update --stamp osg-spec-docs/tasks/audit/framework-context-stamp.json | tee -a "${REPORT_PATH}"
  exit 0
fi

report "❌ FAIL: ${fail_count} checks failed"
report "Stamp update skipped"
exit 1
